function plot_lfp_heatmap(ax, depths_mm, freq_hz, psd_db_matrix, band_limits, cfg)
%PLOT_LFP_HEATMAP  LFP spectral power heatmap — matches Python viz exactly.
%
%   ao.viz.plot_lfp_heatmap(ax, depths, freq, psd_db_matrix, band_limits, cfg)
%
%   Uses imagesc (matching Python imshow) with viridis colormap and
%   5th/95th percentile normalization for consistent contrast.

    arguments
        ax
        depths_mm     (:,1) double
        freq_hz       (:,1) double
        psd_db_matrix (:,:) double
        band_limits   (1,2) double
        cfg           struct
    end

    N = numel(depths_mm);

    % --- Compute depth extent (matching Python _full_depth_limits) ---
    pad_mm = 0.35;
    if N > 0
        d_lo = min(depths_mm) - pad_mm;
        d_hi = max(depths_mm) + pad_mm;
    else
        d_lo = -0.5; d_hi = 0.5;
    end

    % --- 5th/95th percentile normalization (matching Python) ---
    finite_vals = psd_db_matrix(isfinite(psd_db_matrix));
    if numel(finite_vals) >= 2
        clim_lo = prctile(finite_vals, 5);
        clim_hi = prctile(finite_vals, 95);
        if clim_lo >= clim_hi
            clim_lo = median(finite_vals) - 0.5;
            clim_hi = median(finite_vals) + 0.5;
        end
    else
        clim_lo = -10; clim_hi = 10;
    end

    % --- Render with imagesc (matches Python imshow, origin='lower') ---
    % imagesc extent: [freq_min freq_max depth_min depth_max]
    imagesc(ax, 'XData', [freq_hz(1) freq_hz(end)], ...
        'YData', [d_lo d_hi], ...
        'CData', psd_db_matrix);
    set(ax, 'YDir', 'normal');  % origin='lower'
    clim(ax, [clim_lo clim_hi]);

    % --- Viridis colormap (matching Python) ---
    colormap(ax, viridis_cmap());

    % --- Band highlight overlay — Python: #f4d35e, alpha=0.18 ---
    hold(ax, 'on');
    yl = [d_lo, d_hi];
    patch(ax, [band_limits(1) band_limits(2) band_limits(2) band_limits(1)], ...
        [yl(1) yl(1) yl(2) yl(2)], [0.957 0.827 0.369], ...
        'FaceAlpha', 0.18, 'EdgeColor', 'none');
    hold(ax, 'off');

    % --- Colorbar (matching Python: fraction=0.045, pad=0.03) ---
    cb = colorbar(ax, 'Location', 'eastoutside');
    cb.Label.String = 'PSD (dB)';
    cb.FontSize = 8;

    xlabel(ax, 'Frequency (Hz)');
    ylabel(ax, 'Depth (mm)');
    title(ax, 'Spectral Power vs Depth', 'FontSize', 10);

    grid(ax, 'on');
    set(ax, 'GridAlpha', 0.2, 'FontSize', 9, 'Box', 'off');
    xlim(ax, [cfg.lfp.fmin_hz, cfg.lfp.fmax_hz]);
    ax.XAxis.FontSize = 8;
    ax.YAxis.FontSize = 8;
end

